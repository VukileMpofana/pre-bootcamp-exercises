def vowel_check(word):
    word = word.lower()
    sum = 0
    for i in word:
        if (i == 'a'):
            print(word[sum])
        elif (i == 'e'):
            print(word[sum])
        elif (i == 'i'):
            print(word[sum])
        elif (i == 'o'):
            print(word[sum])
        elif (i == 'u'):
            print(word[sum])
        sum = sum + 1
vowel_check("")